#MAYOR:IGUAL:MENOR A 0
numero = int(input('Ingrese número : '))
print('*******************************************')
if numero == 0:
    print('El número ingresado es igual a 0')
else:
    if numero > 0:
        print('El número',numero ,'ingresado es mayor a 0')
    else:
        print('El número',numero ,'ingresado es menor a 0')
print('*******************************************')